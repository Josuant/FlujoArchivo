class Node extends DefaultMutableTreeNode{
    JButton icon;
    boolean isFolder;
    String name;
    Node(String name, boolean isFolder){
        super(name);
        this.name = name;
        this.isFolder = isFolder;
        icon = new JButton(name);
        //icon.setBackground(new java.awt.Color(255, 255, 255));
        icon.setFont(new java.awt.Font("Segoe UI", 1, 18)); // NOI18N
        icon.setForeground(new java.awt.Color(102, 102, 102));
        icon.setIcon(new javax.swing.ImageIcon(getClass().getResource("/img/file.png"))); // NOI18N
        icon.setText(name);
        icon.setBorder(null);
        icon.setFocusPainted(false);
        icon.setFocusable(false);
        icon.setHorizontalAlignment(javax.swing.SwingConstants.LEFT);
        icon.setOpaque(false);    
    }
}

