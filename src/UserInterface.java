    private void actualizar(){
        Node aux = (Node) actual_node.getFirstChild();
        panel_drive.removeAll();
        while(aux!=null){
            panel_drive.add(aux.icon);
            aux = (Node) aux.getNextSibling();
        }
        panel_drive.updateUI();
    }
