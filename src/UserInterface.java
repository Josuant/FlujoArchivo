    private void bt_FolderActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bt_FolderActionPerformed
        // TODO add your handling code here:
        Node newNode = new Node(JOptionPane.showInputDialog(null,"Nueva carpeta"), true);
        Node regresar = new Node("...", true);
        regresar.icon.setIcon(btIfolder);
        newNode.icon.setIcon(btIfolder);
        newNode.add(regresar);
        newNode.icon.addActionListener((ActionEvent evtb) -> {
            actual_node = newNode;
            actualizar();
        });
        regresar.icon.addActionListener((ActionEvent evtb) -> {
            actual_node = (Node)newNode.getParent();
            actualizar();
        });
        actual_node.add(newNode);
        file_tree.setModel(new DefaultTreeModel(raiz));
        actualizar();
        
    }//GEN-LAST:event_bt_FolderActionPerformed

